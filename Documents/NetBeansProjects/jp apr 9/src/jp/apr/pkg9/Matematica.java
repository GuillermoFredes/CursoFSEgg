

package jp.apr.pkg9;


 // @author Camila Astrada 
 
public class Matematica {
private double nuno;
private double ndos;


    public Matematica() {
    }

    public Matematica(double nuno, double ndos) {
        this.nuno = nuno;
        this.ndos = ndos;
    }

    public double getNuno() {
        return nuno;
    }

    public void setNuno(double nuno) {
        this.nuno = nuno;
    }

    public double getNdos() {
        return ndos;
    }

    public void setNdos(double ndos) {
        this.ndos = ndos;
    }

    
}
